export function generateLUT(name: string): Uint8Array {
    const fn = COLORMAP_DEFS[name] || COLORMAP_DEFS.gray;
    const lut = new Uint8Array(256 * 4);
    for (let i = 0; i < 256; i++) {
        const t = i / 255;
        const [r, g, b] = fn(t);
        lut[i * 4] = Math.round(r);
        lut[i * 4 + 1] = Math.round(g);
        lut[i * 4 + 2] = Math.round(b);
        lut[i * 4 + 3] = 255;
    }
    return lut;
}

const lutCache: Record<string, Uint8Array> = {};
export function getLUT(name: string): Uint8Array {
    if (!lutCache[name]) lutCache[name] = generateLUT(name);
    return lutCache[name];
}
