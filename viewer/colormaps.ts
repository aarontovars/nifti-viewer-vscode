function hsvToRgb(h: number, s: number, v: number): [number, number, number] {
    const c = v * s;
    const x = c * (1 - Math.abs(((h / 60) % 2) - 1));
    const m = v - c;
    let r: number, g: number, b: number;
    if (h < 60) { r = c; g = x; b = 0; }
    else if (h < 120) { r = x; g = c; b = 0; }
    else if (h < 180) { r = 0; g = c; b = x; }
    else if (h < 240) { r = 0; g = x; b = c; }
    else if (h < 300) { r = x; g = 0; b = c; }
    else { r = c; g = 0; b = x; }
    return [(r + m) * 255, (g + m) * 255, (b + m) * 255];
}

function lerp3(a: number[], b: number[], t: number): [number, number, number] {
    return [a[0] + (b[0] - a[0]) * t, a[1] + (b[1] - a[1]) * t, a[2] + (b[2] - a[2]) * t];
}

function interpolateStops(stops: number[][], t: number): number[] {
    if (t <= stops[0][0]) return stops[0].slice(1);
    if (t >= stops[stops.length - 1][0]) return stops[stops.length - 1].slice(1);
    for (let i = 0; i < stops.length - 1; i++) {
        if (t >= stops[i][0] && t <= stops[i + 1][0]) {
            const frac = (t - stops[i][0]) / (stops[i + 1][0] - stops[i][0]);
            return lerp3(stops[i].slice(1), stops[i + 1].slice(1), frac);
        }
    }
    return stops[0].slice(1);
}

